    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(50) NOT NULL CHECK (nome IN ('Liga da Justi√ßa', 'Vingadores')),
    descricao VARCHAR(100)
